#Calcular el promedio de 3 números ingresados por teclado y mostrar su resultado# Pedir al usuario que ingrese los números
num_1 = float(input("Ingresa el primer número: "))
num_2 = float(input("Ingresa el segundo número: "))
num_3 = float(input("Ingresa el tercer número: "))


promedio = (num_1 + num_2 + num_3) / 3


print("El promedio es:", promedio)
